let a=5;
let b=7;

/*
 * la parte superior 👆🏻 son declaraciones (declarations)
 * la parte entre paréntesis del console log  es una expresión
 */ 

console.log(a+b, 'suma'); //suma
console.log(a-b, 'resta'); //resta
console.log(a*b, 'multiplicación'); //multiplicación
console.log(a/b, 'división'); //división
console.log(a%b, 'modulo');  //modulo (lo que sobra de la resta)
console.log(a**b, 'potencia');  //potencia

// incrementar
console.log('incrementar')
console.log(++a);  // 6 --> aumenta el valor de a en 1
console.log(a++);  //6 --> muestra el valor de a y luego aumenta 1
console.log(a);   // 7 --> valor de a

console.log('decrementar')

// decrementar
console.log(--a);  // 6 --> de 7 baja a 6
console.log(a--);  //6 --> muestra el valor de a 
console.log(a);   // 5 --> baja el valor de 6 a 5


